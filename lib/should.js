  /**
   * Assert exact keys or inclusion of keys by using
   * the `.include` modifier.
   *
   * @param {Array|String ...} keys
   * @api public
   */

  keys: function(originalKeys){
    var str
      , keys
      , self = this
      , isKeysObject = false
      , blockStr = ''
      , ok = true;

    if (typeof originalKeys === 'object' && !(originalKeys instanceof Array)) {
      isKeysObject = true;
      keys = Object.keys(originalKeys);
    } else {
      keys = originalKeys instanceof Array
        ? originalKeys
        : Array.prototype.slice.call(arguments);
    }

    // Test keys
    if (!keys.length) throw new Error('keys required');

    var actual = Object.keys(this.obj)
      , len = keys.length;

    // make sure they're all present
    ok = keys.every(function(key){
      return ~actual.indexOf(key);
    });

    // matching length
    ok = ok && keys.length === actual.length;

    // Block match
    if (ok && isKeysObject) {
      keys.forEach(function (key) {
        var block = originalKeys[key]
        if (typeof block === 'function') {
          try {
            block(self.obj[key]);
          } catch (err) {
            ok = false;
            blockStr += '\n  ' + key + ': ' + err.message;
          }
        } else {
          if (!eql(block, self.obj[key])) {
            ok = false;
            blockStr += '\n  ' + key + ': ' + 'expected eql ' + should.inspect(block);
          }
        }
      });
    }

    // key string
    if (len > 1) {
      keys = keys.map(function(key){
        return should.inspect(key);
      });
      var last = keys.pop();
      str = keys.join(', ') + ', and ' + last;
    } else {
      str = should.inspect(keys[0]);
    }

    // message
    str = 'have ' + (len > 1 ? 'keys ' : 'key ') + str + blockStr;

    this.assert(
        ok
      , function(){ return 'expected ' + this.inspect + ' to ' + str }
      , function(){ return 'expected ' + this.inspect + ' to not ' + str });

    return this;
  },

